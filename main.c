#include <stdio.h>
#define maxx 87
#define minn 64

int main() {

    int min_max;
    scanf("%d", &min_max);
    printf("Если входит в диапазон указанных значений, то выведется 1,\n если не входит, то 0\n%d  \n", min_max>= minn && min_max<= maxx);

    int count;
    scanf("%d", &count);
    printf("Значение бита:\n%d", (count >> (13-1)) & 1);

    return 0;
}